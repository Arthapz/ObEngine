function DefaultValues(self)
    self.x1 = 0;
    self.y1 = 0;
    self.x2 = 0;
    self.y2 = 0;
    self.x = 0;
    self.y = 0;
    self.width = 0;
    self.height = 0;
    self.rotation = 0;
    self.layer = 0;
    self.color = {
        r = 255,
        g = 255,
        b = 255,
        a = 255
    }
    self.origin = {
        translation = {
            x = 0,
            y = 0
        },
        rotation = {
            x = 0,
            y = 0
        }
    }
end
